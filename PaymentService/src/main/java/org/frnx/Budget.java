package org.frnx;

public class Budget {
    private String budgetIncome;
    private String budgetAccount;


    public String getBudgetIncome() {
        return budgetIncome;
    }

    public void setBudgetIncome(String budgetIncome) {
        this.budgetIncome = budgetIncome;
    }

    public String getBudgetAccount() {
        return budgetAccount;
    }

    public void setBudgetAccount(String budgetAccount) {
        this.budgetAccount = budgetAccount;
    }
}
